    private void findBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_findBtnActionPerformed
        if (areaTxt.getText().isEmpty() && orderNameTxt.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Masukkan Data Yang Ingin Dicari",
                    "ERROR",
                    JOptionPane.ERROR_MESSAGE);
        } else {
            String sql = "SELECT * FROM entri WHERE 1=1";
            if (!areaTxt.getText().isEmpty()) {
                // Filter berdasarkan nama
                sql += " AND nama LIKE '%" + areaTxt.getText() + "%'";
            }
            if (!orderNameTxt.getText().isEmpty()) {
                // Filter berdasarkan area
                sql += " AND area LIKE '%" + orderNameTxt.getText() + "%'";
            }
            showTable(sql);
        }
    }//GEN-LAST:event_findBtnActionPerformed

