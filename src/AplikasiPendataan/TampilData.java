    public void showTable(String sql) {
        try {
            Connection c = Database_client.getKoneksi();
            Statement s = c.createStatement();
            ResultSet r = s.executeQuery(sql);

            tableData.setModel(DbUtils.resultSetToTableModel(r));

            long totalIncome = 0;
            int totalBook = 0;

            // Hitung jumlah baris
            r.last();
            totalBook = r.getRow();
            r.beforeFirst(); // Kembali ke baris pertama

            for (int i = 0; i < totalBook; i++) {
                r.next();
                long income = Long.parseLong(r.getString(8)); // Ganti dengan indeks kolom yang sesuai
                totalIncome += income;
            }

            // Setelah loop, atur nilai totalBookTxt dan totalIncomeTxt
            totalBookTxt.setText("" + totalBook);
            totalIncomeTxt.setText("" + totalIncome);

            r.close();
            s.close();
        } catch (SQLException ex) {
            Logger.getLogger(TampilData.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
